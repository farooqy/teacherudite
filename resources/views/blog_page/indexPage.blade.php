                        <div class="post-text">
                            <p>China is a country shrouded in mystery for many foreigners, especially those from
                                far-off countries like the USA, Canada, and the United Kingdom. Many prospective
                                teachers who reach out to us are a little wary of teaching in China because there
                                are some common misconceptions about what it’s really like to teach there. Well,
                                we’re here to set the record straight. Read on to discover some of the
                                misconceptions people have about teaching English in China, and find out the truth
                                for yourself!</p>
                            <h4>Pay Rate for Teaching in China</h4>
                            <p>Some applicants are under the impression that teachers in China tend to make less
                                money than those working in other regions like the Middle East — that’s false! A
                                quick survey of job postings on our site and others shows that while the average
                                starting salary for qualified overseas English teachers in China may be on the lower
                                end of average, all-in-all, it’s pretty close to par with other comparable overseas
                                locations (depending on work experience, of course). Further, there are many
                                positions in China with higher and more competitive salaries, depending on
                                experience.
                            </p>
                            <p>
                                Taking all of that into account and factoring in the comparably low cost of living
                                in China, teachers there can actually end up pocketing more of their take-home pay
                                than in places like Oman where groceries and entertainment cost significantly more.
                            </p>
                            <p>
                                Reality: Teachers can expect a starting salary of $2000-2500 USD for most of our
                                placements in China, compared to a starting average of $2500-3500 USD in the Middle
                                East, but costs of living balance out the equation making take-home pay comparable
                                across both locations.</p>
                            <div class="row">
                                <div class="col-md">
                                    <img src="../images/blog/blog-post-img-4.jpg" class="img-fluid" alt=""
                                        style="width: 100%;">
                                </div>
                                <div class="col-md mt-2 mt-md-0">
                                    <p><b>Local Culture &amp; Value of Teachers</b> Some people are under the crazy
                                        notion that teachers in China (and foreigners in general) get less respect
                                        from locals and students than they do in other countries — this couldn’t be
                                        further from the truth! A study by the University of Sussex put this
                                        question to rest with extensive data, finding that public respect for
                                        teachers was actually higher in China than anywhere else in the world,
                                        including the USA and the UK. In fact, the Chinese actually place teachers
                                        in the same esteem as doctors. China places a huge cultural value on
                                        education, and many students are eager to learn English, earning teachers a
                                        high positive regard from locals.</p>
                                    <p><b>Reality:</b> China values education, meaning you’ll be highly regarded by
                                        students and locals as a teacher, and treated accordingly. Don’t believe us?
                                        Check out the research-backed data for yourself!</p>
                                </div>
                            </div>
                            <div class="mt-3"></div>
                            <h4>Longevity of Career Prospects</h4>
                            <p>One other weird misconception people have of teaching in China is that teachers are
                                easily replaced. Having established that teachers in China are highly valued by the
                                locals and their students, you might have a hard time believing this myth already,
                                but to erase any kernels of doubt, you should know that most of our placements
                                require a set time commitment of 1 to 2 years. Schools have no desire to replace
                                teachers on any kind of regular basis, so flights are reimbursed only on successful
                                completion of the term, and bonuses are often paid out as an extra incentive for
                                teachers to stick around.</p>
                            <p><b>Reality:</b> Schools in China are happy when they get a teacher who will commit to
                                stick around, and employers will reward you accordingly. Remember, teachers are
                                highly valued!</p>
                            <p>Living and working in China is an incredible experience, and teachers are treated
                                very well. If you’re ready to see for yourself, have a look at our vacant positions
                                in the region, and apply today!</p>
